class TypeCasting {
    public static void main(String[] args) {

        double d = 10.75;
        int i = (int)d;

        System.out.println("Double Value = " + d);
        System.out.println("Converted Int = " + i);

        int num = 25;
        double result = (double)num;

        System.out.println("Integer = " + num);
        System.out.println("Converted Double = " + result);
    }
}